/**
 * Helper function to create icon element
 * @param {string} name - Icon name from icons object
 * @param {string} className - Optional CSS class
 * @returns {HTMLElement} Icon element
 */
export function createIcon(name, className = '') {
  const container = document.createElement('span');
  container.className = `icon ${className}`;
  container.innerHTML = icons[name] || icons.sparkles;
  return container;
}

/**
 * Get raw SVG string for inline use
 * @param {string} name - Icon name
 * @returns {string} SVG string
 */
export function getIconSVG(name) {
  return icons[name] || icons.sparkles;
}

